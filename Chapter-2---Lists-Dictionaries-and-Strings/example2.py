#------------------------------------------------------------------------------
# Create a 1-Dimensional List
print("\nPart 1 \n")

a = [1, 2, 3, 4, 5]
print(f"1D List: {a}")

# Reverse the List
rev_a = a[::-1]
print(f"1D List Reversed: {rev_a}")

# Capture a Sub-List
sub_a = a[1:4]
print(f"1D Sub-List: {sub_a}")