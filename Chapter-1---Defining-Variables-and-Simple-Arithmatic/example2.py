#------------------------------------------------------------------------------
# Integer Data-Type Example
print('Part 1 \n')

# Declare an Integer
a = 21
print(a)

# Print Binary, Octal, and Hexidecimal Forms of 21
print(0b10101) # Binary
print(0o25)    # Octal
print(0x15)    # Hexidecimal

# -----------------------------------------------------------------------------
# Integer Data-Type Example
print('\nPart 2 \n')

# Declare Floating Point Numbers
a = 12.3
b = 0.123e3
c = 123e-13

# Print Results
print(f"a = {a}")
print(f"b = {b}")
print(f"c = {c} \n")

#------------------------------------------------------------------------------
# Complex DataTypes Example
print('Part 3 \n')

# Declare Complex Number
a = 3 + 4j
b = 0 + 1j

# Print Complex Numnbers
print(a)
print(b)

#------------------------------------------------------------------------------
# String DataType Example
print('\nPart 4 \n')

# Declare Strings with single and double quotes
str1 = 'hello world'
str2 = "hello world"

# Are these strings the same?
print(f"Are str1 and str2 the same? Answer: {str1 == str2} \n")

